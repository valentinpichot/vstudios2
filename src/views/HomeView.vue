<script setup lang="ts">
import { onMounted } from 'vue'
import { gsap } from 'gsap'
import { ScrollTrigger } from 'gsap/ScrollTrigger'
import { ScrollSmoother } from 'gsap/ScrollSmoother'
import { isMobile } from 'mobile-device-detect'
import { SplitText } from 'gsap/SplitText'
import ShowcaseView from '@/views/ShowcaseView.vue'

gsap.registerPlugin(ScrollTrigger, ScrollSmoother, SplitText)
onMounted(() => {
  const durationCreative = '0.7'
  const paths = document.querySelectorAll('.creative path')
  const imgs = document.querySelectorAll('.images img')
  const imgsContainer = document.querySelector('.images')
  const creative = document.querySelector('.creative')
  const heroTransiBot = document.querySelector('.hero-transi-bot')
  const homePrez = document.querySelector('.home-prez')
  const homePrezParallax = document.querySelector('.parallax-slab')
  const parallaxSlabAbs = document.querySelector('.parallax-slab-abs')
  const preloaderDuration = 4000
  const colorBlue = 'rgba(61, 77, 148, 0.76)'
  const parallaxTexts = document.querySelectorAll('.parallax-text p')

  const smoother = ScrollSmoother.create({
    wrapper: '#smooth-wrapper',
    content: '#smooth-content',
    normalizeScroll: false,
    ignoreMobileResize: true,
    smooth: 2,
    smoothTouch: 2,
    effects: true
  })
  ScrollTrigger.normalizeScroll(true)
  let mySplitText = new SplitText('#split-stagger', { type: 'words,chars' })
  let mySplitTextShowcase = new SplitText('#split-stagger-showcase', { type: 'words,chars' })
  let chars = mySplitText.chars
  let charsShowcase = mySplitTextShowcase.chars

  chars.forEach((char, i) => {
    smoother.effects(char, { speed: 1, lag: (i + 1) * 0.1 })
  })
  charsShowcase.forEach((char, i) => {
    smoother.effects(char, { speed: 1, lag: (i + 1) * 0.1 })
  })
  ScrollTrigger.create({
    trigger: '#smooth-wrapper',
    start: 'top top',
    end: '+=1%',
    scrub: false,
    onEnter: () => {
      gsap.to(heroTransiBot, {
        ease: 'linear',
        duration: 1,
        y: isMobile ? -10 : -50,
        yoyo: true
      })
    }
  })
  ScrollTrigger.create({
    trigger: homePrez,
    start: 'top 60%',
    end: '+=70%',
    scrub: 1,
    toggleActions: 'play reverse play reverse',
    animation: gsap.fromTo(
      homePrezParallax,
      {
        ease: 'linear',
        duration: 1,
        width: '90%',
        borderRadius: '15px',
        transform: 'scale(1)',
        toggleActions: 'play reverse play reverse',
        yoyo: true
      },
      {
        ease: 'linear',
        duration: 1,
        width: '100%',
        yoyo: true,
        borderRadius: 0,
        transform: 'scale(1.1)',
        toggleActions: 'play reverse play reverse'
      }
    )
  })
  ScrollTrigger.create({
    trigger: homePrez,
    start: 'top 60%',
    end: '+=70%',
    scrub: 1,
    toggleActions: 'play reverse play reverse',
    animation: gsap.to(parallaxSlabAbs, {
      ease: 'power4',
      duration: 1,
      autoAlpha: 1,
      y: -1,
      yoyo: true
    })
  })
  parallaxTexts.forEach((parallaxText, index) => {
    gsap.fromTo(
      parallaxText,
      {
        autoAlpha: 0
      },
      {
        autoAlpha: 1,
        y: isMobile ? 50 * index : 80 * index,
        duration: 0.5,
        ease: 'power2.inOut',
        delay: 0.2 * index,
        scrollTrigger: {
          trigger: homePrez,
          start: 'top 60%',
          end: '+=70%',
          scrub: 1,
          toggleActions: 'play reverse play reverse'
        }
      }
    )
  })

  paths.forEach((path, index) => {
    // Configure le ScrollTrigger
    const delay = index * 0.2 // Exemple : un délai de 0.2 secondes entre chaque animation
    gsap.to(path, {
      scale: 1.1,
      duration: 1,
      delay: delay,
      repeat: -1, // Animation infinie
      yoyo: true, // Retour à l'état initial après l'animation
      transformOrigin: 'center center' // Spécifie l'origine de la transformation
    })
    ScrollTrigger.create({
      trigger: path,
      start: '-=150 -=30',
      end: '+=1%',
      scrub: true,
      onEnter: () => {
        gsap.to(path, {
          fill: '#293b8a',
          ease: 'power1.inOut',
          duration: durationCreative,
          stagger: 0.1,
          delay: 0
        })
      },
      onLeave: () => {
        gsap.to(path, {
          fill: colorBlue,
          stagger: 0.1,
          duration: durationCreative,
          ease: 'power1.inOut',
          delay: delay
        })
      },
      onLeaveBack: () => {
        gsap.to(path, {
          fill: '#182457',
          duration: durationCreative,
          ease: 'power1.inOut',
          delay: delay
        })
      }
    })
  })
  setTimeout(() => {
    appear()
  }, preloaderDuration)

  function appear() {
    console.log('appear')
    gsap.to(imgs, {
      objectPosition: '100% 100%',
      duration: isMobile ? 20 : 10,
      ease: isMobile ? 'linear' : 'power4.inOut',
      delay: 0,
      yoyo: true, // Retour à l'état initial après l'animation
      repeat: -1 // Animation infinie
    })
    gsap.to(imgsContainer, {
      top: isMobile ? '15vh' : '30vh',
      duration: 2,
      ease: 'power4.inOut',
      delay: 0,
      yoyo: true // Retour à l'état initial après l'animation
    })
    gsap.to(creative, {
      width: isMobile ? '95%' : '150vh',
      duration: 2,
      ease: 'power4.inOut',
      delay: 0,
      yoyo: true // Retour à l'état initial après l'animation
    })
  }
})
</script>
